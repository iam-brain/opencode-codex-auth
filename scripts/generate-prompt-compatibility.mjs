import { createHash } from "node:crypto"
import { mkdir, readFile, rename, writeFile } from "node:fs/promises"
import { dirname, isAbsolute, relative, resolve, sep } from "node:path"
import { fileURLToPath, pathToFileURL } from "node:url"

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..")
const rulesPath = resolve(root, "prompts/prompt-compatibility.rules.json")

function sha256(value) {
  return createHash("sha256").update(value).digest("hex")
}

function resolveWithin(base, candidate, label) {
  if (isAbsolute(candidate)) throw new Error(`${label} must be relative`)
  const resolved = resolve(base, candidate)
  const relativePath = relative(base, resolved)
  if (relativePath === ".." || relativePath.startsWith(`..${sep}`) || isAbsolute(relativePath)) {
    throw new Error(`${label} must stay within ${base}`)
  }
  return resolved
}

function countOccurrences(value, search) {
  if (!search) throw new Error("replacement literal must not be empty")
  return value.split(search).length - 1
}

function extractRustStringConstant(source, constant) {
  const marker = `const ${constant}: &str = `
  const start = source.indexOf(marker)
  if (start < 0) throw new Error(`missing Rust string constant ${constant}`)
  const valueStart = start + marker.length
  const valueEnd = source.indexOf(";", valueStart)
  if (valueEnd < 0) throw new Error(`unterminated Rust string constant ${constant}`)
  const literal = source.slice(valueStart, valueEnd).trim()
  try {
    const parsed = JSON.parse(literal)
    if (typeof parsed !== "string") throw new Error("constant is not a string")
    return parsed
  } catch (error) {
    throw new Error(`invalid Rust string literal for ${constant}: ${error instanceof Error ? error.message : error}`)
  }
}

function applyOperations(value, operations, exportName) {
  let rendered = value
  for (const [index, operation] of operations.entries()) {
    if (operation.type !== "replace_literal") {
      throw new Error(`${exportName} operation ${index + 1}: unsupported type ${operation.type}`)
    }
    const actualCount = countOccurrences(rendered, operation.old)
    if (actualCount !== operation.expectedCount) {
      throw new Error(
        `${exportName} operation ${index + 1}: expected ${operation.expectedCount} matches, found ${actualCount}`
      )
    }
    rendered = rendered.replaceAll(operation.old, operation.new)
  }
  return rendered
}

async function loadRules() {
  return JSON.parse(await readFile(rulesPath, "utf8"))
}

async function validateSources(rules) {
  const sources = new Map()
  for (const source of rules.sources) {
    if (sources.has(source.id)) throw new Error(`duplicate source id ${source.id}`)
    const path = resolveWithin(root, source.path, `${source.id} path`)
    const content = await readFile(path)
    const actualHash = sha256(content)
    if (actualHash !== source.sha256) {
      throw new Error(`${source.id} hash mismatch: expected ${source.sha256}, got ${actualHash}`)
    }
    const text = content.toString("utf8")
    for (const symbol of source.requiredSymbols) {
      if (!text.includes(symbol)) throw new Error(`${source.id}: missing evidence symbol ${JSON.stringify(symbol)}`)
    }
    sources.set(source.id, text)
  }
  return sources
}

function renderArtifacts(rules, sources) {
  const artifacts = []
  for (const output of rules.outputs) {
    const source = sources.get(output.sourceId)
    if (!source) throw new Error(`${output.exportName}: unknown source ${output.sourceId}`)
    const extracted = extractRustStringConstant(source, output.rustConstant)
    const transformed = applyOperations(extracted, output.operations, output.exportName)
    const wrapped = `<multi_agent_mode>\n${transformed}\n</multi_agent_mode>`
    artifacts.push({ exportName: output.exportName, value: wrapped })
  }
  return artifacts
}

function renderModule(artifacts) {
  const lines = [
    "// Generated by scripts/generate-prompt-compatibility.mjs. Do not edit directly.",
    "// Derived from OpenAI Codex and modified for OpenCode compatibility under Apache-2.0.",
    "// See prompts/upstream/licenses/openai-codex-LICENSE and openai-codex-NOTICE.",
    ""
  ]
  for (const artifact of artifacts) {
    lines.push(`export const ${artifact.exportName} =`, `  ${JSON.stringify(artifact.value)}`, "")
  }
  return `${lines.join("\n").trimEnd()}\n`
}

async function main() {
  const check = process.argv.includes("--check")
  const checkDist = process.argv.includes("--check-dist")
  const rules = await loadRules()
  const sources = await validateSources(rules)
  const artifacts = renderArtifacts(rules, sources)
  const rendered = renderModule(artifacts)
  const outputPath = resolveWithin(root, rules.output, "prompt output")

  if (checkDist) {
    if (!rules.output.endsWith(".ts")) throw new Error("prompt output must be a TypeScript module")
    const relativeDistPath = rules.output.replace(/\.ts$/, ".js")
    const distRoot = resolve(root, "dist")
    const compiledPath = resolveWithin(distRoot, relativeDistPath, "compiled prompt output")
    const compiled = await import(`${pathToFileURL(compiledPath).href}?check=${Date.now()}`)
    const actualExports = Object.keys(compiled).sort()
    const expectedExports = artifacts.map((artifact) => artifact.exportName).sort()
    if (JSON.stringify(actualExports) !== JSON.stringify(expectedExports)) {
      throw new Error(
        `compiled prompt artifact exports differ: expected ${expectedExports.join(", ")}, got ${actualExports.join(", ")}`
      )
    }
    for (const artifact of artifacts) {
      if (compiled[artifact.exportName] !== artifact.value) {
        throw new Error(`compiled prompt artifact is missing ${artifact.exportName}: ${relativeDistPath}`)
      }
    }
    process.stdout.write(`Compiled prompt artifact contains ${artifacts.length} overlays.\n`)
    return
  }

  if (check) {
    const current = await readFile(outputPath, "utf8").catch(() => undefined)
    if (current !== rendered) {
      throw new Error(`generated prompt is out of date: run npm run generate:prompts`)
    }
    process.stdout.write(`Prompt compatibility check passed for ${rules.outputs.length} overlays.\n`)
    return
  }

  await mkdir(dirname(outputPath), { recursive: true })
  const temporaryPath = `${outputPath}.tmp`
  await writeFile(temporaryPath, rendered, "utf8")
  await rename(temporaryPath, outputPath)
  process.stdout.write(`Generated ${rules.output}.\n`)
}

main().catch((error) => {
  process.stderr.write(`Prompt compatibility error: ${error instanceof Error ? error.message : error}\n`)
  process.exitCode = 1
})
